let input = +prompt("Nhập vào số bạn muốn đọc (0-9): ");

switch (input) {
    case 0:
        alert("Số không!");
        break;
    case 1:
        alert("Số một!");
        break;
    case 2:
        alert("Số hai!");
        break;
    case 3:
        alert("Số ba!");
        break;
    case 4:
        alert("Số bốn!");
        break;
    case 5:
        alert("Số năm!");
        break;
    case 6:
        alert("Số sáu!");
        break;
    case 7:
        alert("Số bảy!");
        break;
    case 8:
        alert("Số tám!");
        break;
    case 9:
        alert("Số chín!");
        break;
    default:
        alert("Chọn số từ 0-9 thôi bro!");
}