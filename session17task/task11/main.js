// Sử dụng Math.abs()
console.log(Math.abs(-5.5)); // Kết quả: 5.5, vì Math.abs() trả về giá trị tuyệt đối của số (-5.5 -> 5.5)
console.log(Math.abs(10));   // Kết quả: 10, vì 10 đã là số dương nên không thay đổi

// Sử dụng Math.ceil()
console.log(Math.ceil(4.4)); // Kết quả: 5, vì Math.ceil() làm tròn lên số nguyên gần nhất
console.log(Math.ceil(5.8)); // Kết quả: 6, vì Math.ceil() luôn làm tròn lên dù phần thập phân nhỏ hay lớn

// Sử dụng Math.floor()
console.log(Math.floor(5.7)); // Kết quả: 5, vì Math.floor() làm tròn xuống số nguyên nhỏ hơn gần nhất
console.log(Math.floor(6.1)); // Kết quả: 6, vì Math.floor() bỏ phần thập phân và giữ lại số nguyên

// Sử dụng Math.round()
console.log(Math.round(10.1)); // Kết quả: 10, vì Math.round() làm tròn gần nhất, 10.1 gần 10 hơn
console.log(Math.round(20.6)); // Kết quả: 21, vì Math.round() làm tròn gần nhất, 20.6 gần 21 hơn
